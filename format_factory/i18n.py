def detect_system_language() -> str:
    name = (QLocale.system().name() or "").replace("-", "_")
    name = name.split(".")[0]
    if name.startswith("zh_TW") or name.startswith("zh_HK") or name.startswith("zh_MO"):
        return LANG_ZH_TW
    if name.startswith("zh"):
        return LANG_ZH_CN
    if name.startswith("ja"):
        return LANG_JA
    if name.startswith("ko"):
        return LANG_KO
    return LANG_EN


def resolve_language(language: str) -> str:
    if language in {LANG_ZH_CN, LANG_ZH_TW, LANG_EN, LANG_JA, LANG_KO}:
        return language
    return detect_system_language()


