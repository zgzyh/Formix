def translate_preset_label(language: str, name: str) -> str:
    lang = resolve_language(language)
    if lang == LANG_ZH_CN:
        return name
    translated = name
    replacements = sorted(
        _PRESET_LABEL_REPLACEMENTS.get(lang, []),
        key=lambda item: len(item[0]),
        reverse=True,
    )
    for src, dst in replacements:
        translated = translated.replace(src, dst)
    return translated


